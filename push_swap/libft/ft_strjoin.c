#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	char	*res;

	if (!s1 || !s2)
		return (NULL);
	res = malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!res)
		return (NULL);
	i = ft_strlen(s1);
	j = ft_strlen(s2);
	ft_strlcpy(res, s1, i + 1);
	ft_strlcpy(res + i, s2, j + 1);
	return (res);
}
